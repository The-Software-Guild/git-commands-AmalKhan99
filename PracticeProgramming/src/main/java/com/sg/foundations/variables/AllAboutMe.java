package com.sg.foundations.variables;

public class AllAboutMe {
    public static void main(String[]args){
        String name, favFood, residence;
        int noOfPets;
        boolean whistle;

        name= "Amal";
        favFood = "Pasta";
        residence= "house";
        noOfPets= 1;
        whistle= true;

        System.out.println("My name is "+name);
        System.out.println("I live in a "+residence);
        System.out.println("My favourite food is "+favFood);
        System.out.println("I have "+noOfPets+" pets/s");
        System.out.println("And it is "+whistle+" that I can whistle");
    }
}
